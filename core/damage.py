"""
Слой конвертации удара в сдвиг оси ("сколько реально сдвинулось от попадания").

Это ОТДЕЛЬНЫЙ слой от core/cushion.py. Разделение специально:

  - cushion.py отвечает на вопрос "далеко ли до слома" (сколько ещё можно
    сдвинуть от природной точки существа, прежде чем сработает летальный эффект).
  - damage.py (этот файл) отвечает на вопрос "а насколько ударом РЕАЛЬНО
    сдвинуло" — и вот здесь как раз живут немонотонные (параболические) кривые
    и множители-от-других-осей, которых пока не было видно в демке, потому что
    мы до сих пор строили только первый слой.

Итоговый пайплайн одного удара:
    raw_shift = ... (сила удара, из механизма/навыка)
    actual_shift = raw_shift * kinetic_density_curve(defender.density)  [если механизм="кинетика"]
                            * size_multiplier(defender.density)          [общий модификатор для matter/energy]
    -> actual_shift подаётся в cushion.py как displacement для проверки на слом.

ВНИМАНИЕ: это первый черновой проход, не финальная физика. Числа (положение
пика, ширина, крутизна) подобраны для ДЕМОНСТРАЦИИ формы кривой, а не откалиброваны
под конкретный баланс боя — калибровка это отдельная итерация после того, как вы
увидите форму и решите, такая ли она должна быть.
"""

from .vector import clamp


def kinetic_density_curve(density: float) -> float:
    """Немонотонная (П-образная) кривая специально для механизма 'Кинетика'
    против matter.density — та самая история с гоблином и дубиной:

        density = -1.0  -> 0.0   (дубина проходит насквозь, полный иммунитет)
        density = -0.8  -> ПИК   (самый уязвимый: не проходит насквозь, но и
                                   недостаточно плотный, чтобы гасить удар)
        density = +1.0  -> 0.35  (броня: удар доходит, но сильно ослаблен)

    Форма: линейный подъём от иммунитета к пику, затем линейный спад к броне.
    Не гладкая парабола, но именно тот немонотонный (не линейный) профиль,
    который мы обсуждали и который отсутствовал в коде.
    """
    d = clamp(density)
    peak_at = -0.80
    peak_val = 1.6
    floor_val_at_immune = 0.0
    floor_val_at_armored = 0.35

    if d <= peak_at:
        # от полного иммунитета (d=-1) до пика (d=peak_at)
        t = (d - (-1.0)) / (peak_at - (-1.0))  # 0..1
        return peak_val * clamp(t, 0.0, 1.0) + floor_val_at_immune
    else:
        # от пика вниз к броне на d=+1
        t = (d - peak_at) / (1.0 - peak_at)  # 0..1
        return peak_val - (peak_val - floor_val_at_armored) * clamp(t, 0.0, 1.0)


def size_multiplier(density: float) -> float:
    """Общий (не привязанный к конкретному механизму) множитель входящего
    урона по векторам МАТЕРИИ и ЭНЕРГИИ — 'чем больше существо, тем меньше ему
    вредит удар дубиной, чем меньше — тем эффективнее по нему сработает даже
    плевок'. В отличие от kinetic_density_curve выше, эта кривая МОНОТОННА и
    не специфична для кинетики — она работает как общий 'масштаб прочности'
    поверх любой специфичной для механизма кривой.

        density = -1.0 (микроскопический) -> x1.8 (втрое уязвимее среднего)
        density =  0.0 (средний)           -> x1.0
        density = +1.0 (колоссальный)      -> x0.35 (сильно тяжелее продавить)
    """
    d = clamp(density)
    return clamp(1.0 - 0.65 * d, 0.35, 1.8)


def resolve_kinetic_hit(raw_magnitude: float, defender_density: float) -> float:
    """Полный пример пайплайна для механизма 'Кинетика' (удар дубиной) по
    matter.density: базовая сила удара x парабола-кривая x общий размер-множитель."""
    return raw_magnitude * kinetic_density_curve(defender_density) * size_multiplier(defender_density)
